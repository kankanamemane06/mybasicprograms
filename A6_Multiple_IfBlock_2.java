package basic.program;

public class A6_Multiple_IfBlock_2 {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		
		
		 if(a<b)
		 {
			 System.out.println("A<B");
		 }
		 
		 if(a>b)
		 {
			 System.out.println("A>B");
		 }
		 
		 if(a>=b)
		 {
			 System.out.println("A>=B");
		 }
		 
		 if(a<=b)
		 {
			 System.out.println("A<=B");
		 }
		 
		 if(a==b)
		 {
			 System.out.println("A==B");
		 }
		 if(a!=b)
		 {
			 System.out.println("A!=B");
		 }
	}

}
