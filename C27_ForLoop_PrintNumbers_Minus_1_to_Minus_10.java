//Assignment 27=Iterate from -1 to -10 using For Loop

package loops;

public class C27_ForLoop_PrintNumbers_Minus_1_to_Minus_10 {

	public static void main(String[] args) {
		System.out.println("Iterate  from -1 to -10 using For Loop=");
		
		for(int i=-1;i>=-10;i--)
			System.out.println(i);
	}

	

}
