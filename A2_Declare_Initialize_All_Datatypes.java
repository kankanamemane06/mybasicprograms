//Assignment 2=Declaring and Initializing all the Datatypes in Java

package basic.program;

public class A2_Declare_Initialize_All_Datatypes {

	public static void main(String[] args) {
		
		System.out.println("Declare and Initialize Byte Datatype");
		byte a1=10;
		System.out.println(a1);
		
		System.out.println("Declare and Initialize Short Datatype");
		short a2=9874;
		System.out.println(a2);
		
		System.out.println("Declare and Initialize Int Datatype");
		int a3=908765;
		System.out.println(a3);
		
		System.out.println("Declare and Initialize Long Datatype");
		long a4=9876546;
		System.out.println(a4);
		
		System.out.println("Declare and Initialize Float Datatype");
		float a5=6.786f;
		System.out.println(a5);
		
		System.out.println("Declare and Initialize Double Datatype");
		double a6=8.9876543;
		System.out.println(a6);
		
		System.out.println("Declare and Initialize Char Datatype");
		char a7='A';
		System.out.println(a7);
		
		System.out.println("Declare and Initialize String Datatype");
		String name="Kankana";
		System.out.println(name);
		
		System.out.println("Declare and Initialize Boolean Datatype");
		boolean a8=true;
		System.out.println(a8);
		

	}

}
